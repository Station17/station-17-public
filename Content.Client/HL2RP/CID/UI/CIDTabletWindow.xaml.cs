using Content.Shared.HL2RP.CID.UI;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;

namespace Content.Client.HL2RP.CID.UI;

[GenerateTypedNameReferences]
public sealed partial class CIDTabletWindow : BaseWindow
{
    private List<CIDDatabaseRecord> _records = new();
    private CIDRecordDetailsWindow? _detailsWindow;

    public Action? OnGenerateNumber;
    public Action<string, string, string>? OnWriteCard;
    public Action<NetEntity>? OnSelectRecord;
    public Action<NetEntity, int>? OnChangeRecordLp;

    public CIDTabletWindow()
    {
        RobustXamlLoader.Load(this);

        Tabs.SetTabTitle(0, "Информация");
        Tabs.SetTabTitle(1, "База данных");
        Tabs.SetTabTitle(2, "Выдача");

        CloseButton.OnPressed += _ => Close();
        GenerateNumberButton.OnPressed += _ => OnGenerateNumber?.Invoke();
        WriteCardButton.OnPressed += _ =>
            OnWriteCard?.Invoke(IssueNameLine.Text, IssueSurnameLine.Text, IssueNumberLine.Text);

        RecordsList.OnItemSelected += args =>
        {
            if (args.ItemIndex < 0 || args.ItemIndex >= _records.Count)
                return;

            OnSelectRecord?.Invoke(_records[args.ItemIndex].CardUid);
        };
    }

    public void UpdateState(CIDTabletBoundUiState state)
    {
        InfoNameLabel.Text = $"Имя: {state.Name}";
        InfoSurnameLabel.Text = $"Фамилия: {state.Surname}";
        InfoNumberLabel.Text = $"Номер гражданина: {state.CNumber}";
        InfoLpLabel.Text = $"LP: {state.LPCount}";
        InfoJobLabel.Text = $"Должность: {state.Job}";

        Tabs.SetTabVisible(2, state.CanIssue);
        IssueStatusLabel.Text = state.HasIssueCard
            ? "Карта для выдачи вставлена"
            : "Вставьте пустую CID карту в слот выдачи";

        if (!string.IsNullOrWhiteSpace(state.GeneratedNumber))
            IssueNumberLine.Text = state.GeneratedNumber;

        UpdateRecords(state.Records);
        UpdateDetailsPopup(state.SelectedRecord, state.CanViewDetails);
    }

    private void UpdateRecords(List<CIDDatabaseRecord> records)
    {
        _records = records;
        RecordsList.Clear();
        foreach (var record in records)
        {
            RecordsList.AddItem($"{record.Name} {record.Surname} [{record.CNumber}]");
        }
    }

    private void UpdateDetailsPopup(CIDRecordDetails? details, bool canViewDetails)
    {
        if (!canViewDetails || details == null)
        {
            _detailsWindow?.Close();
            _detailsWindow = null;
            return;
        }

        if (_detailsWindow == null || _detailsWindow.Disposed)
        {
            _detailsWindow = new CIDRecordDetailsWindow();
            _detailsWindow.OnApplyLp += (uid, lp) => OnChangeRecordLp?.Invoke(uid, lp);
            _detailsWindow.OpenCentered();
        }

        _detailsWindow.SetData(details, canViewDetails);
    }
}

public sealed class CIDRecordDetailsWindow : DefaultWindow
{
    private readonly Label _name = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _surname = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _number = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _level = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _tokens = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _job = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly Label _access = new() { FontColorOverride = Color.FromHex("#D8D8D8") };
    private readonly LineEdit _lpEdit = new() { MinSize = (120, 0) };
    private readonly Button _apply = new() { Text = "Применить LP" };

    private NetEntity _currentUid;
    public Action<NetEntity, int>? OnApplyLp;

    public CIDRecordDetailsWindow()
    {
        Title = "Данные CID";
        MinSize = (360, 320);
        var root = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            SeparationOverride = 4
        };

        root.AddChild(_name);
        root.AddChild(_surname);
        root.AddChild(_number);

        var lpRow = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, SeparationOverride = 4 };
        lpRow.AddChild(new Label { Text = "LP:", FontColorOverride = Color.FromHex("#D8D8D8") });
        lpRow.AddChild(_lpEdit);
        lpRow.AddChild(_apply);
        root.AddChild(lpRow);

        root.AddChild(_level);
        root.AddChild(_tokens);
        root.AddChild(_job);
        root.AddChild(_access);
        Contents.AddChild(root);

        _apply.OnPressed += _ =>
        {
            if (int.TryParse(_lpEdit.Text, out var lp))
                OnApplyLp?.Invoke(_currentUid, lp);
        };
    }

    public void SetData(CIDRecordDetails details, bool canEditLp)
    {
        _currentUid = details.CardUid;
        _name.Text = $"Имя: {details.Name}";
        _surname.Text = $"Фамилия: {details.Surname}";
        _number.Text = $"Номер: {details.CNumber}";
        _lpEdit.Text = details.LPCount.ToString();
        _lpEdit.Editable = canEditLp;
        _apply.Disabled = !canEditLp;
        _level.Text = $"LP уровень: {details.LPLevel}";
        _tokens.Text = $"Токены: {details.TokensCount}";
        _job.Text = $"Должность: {details.Job}";
        _access.Text = $"Access: {details.Access}";
    }
}
